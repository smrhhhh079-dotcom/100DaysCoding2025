package Day25;
public class Main {
    
    public static void main(String[] args) {
        
        int a = 2;
        int b = 3;

        System.out.println("a++ = " + (a++));
        System.out.println("++a = " + (++a));
        System.out.println("b-- = " + (b--));
        System.out.println("--b = " + (--b));
    }
}
