package Days4.java;

public class Main {
    
    public static void main(String[] args) {
        System.out.println("Hello \nWorld");
        System.out.println("Perkenalkan nama saya\bSamra");
        System.out.println("Tanggal lahir\t 17 April 2006");
        System.out.println("Saya Kuliah di \"Universitas Sulawesi Barat\"");
        System.out.println("Saya mengambil prodi \'informatika\' dengan NIM D0225019");
        
       // Beberapa Fungsi pengguna Escape Sequence 
      // \n -> untuk baris baru
      // \b -> untuk menghapus satu karakter
      // \t -> untuk memberikan spasi didepan karakter 
      // \\ -> untuk menampilkan backslash
      // \" -> untuk menambah tanda kutip pada samping atas kata 
      // \' -> untuk menambah tanda koma atas 
    }
    
}
