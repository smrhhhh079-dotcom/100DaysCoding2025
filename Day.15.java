package Day15.java;
public class Main {
    public static void main(String[] args) {
        
        int a = 10;
        int b = 5;

        //Penjumlahan
        int hasilTambah = a + b;

        //Pengurangan
        int hasilKurang = a - b;

        //Output
        System.out.println("Nilai a = " + a);
        System.out.println("Nilai b = " + b);
        System.out.println("Hasil Penjumlahan (a + b) = " + hasilTambah);
        System.out.println("Hasil Pengurangan (a - b) = " + hasilKurang);
    }
}
    
    
