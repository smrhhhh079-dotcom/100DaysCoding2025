package Day09.java;

public class Main {
    
    public static void main(String[] args) {
        
        //Deklarasi variabel
        int umur;
        String nama;

        //Inisialisasi variabel
        umur = 18;
        nama = "Sam";

        //Deklarasi + inisialisasi langsung
        double tinggi = 144;

        // Menampilkan nilai awal
        System.out.println("Nama   : " + nama);
        System.out.println("Umur   : " + umur);
        System.out.println("Tinggi : " + tinggi);

        //Update variabel
        umur = 19;
        tinggi = 145;
        nama = "Samra";

        // Menampilkan nilai setelah update
        System.out.println("\nSetelah update:");
        System.out.println("Nama   : " + nama);
        System.out.println("Umur   : " + umur);
        System.out.println("Tinggi : " + tinggi);
    

    }
    
}
